#include<stdio.h>
int main(){
	int age = 22 ; 
    float pi = 3.14159 ; 
    char star = '*' ; 
    
    printf("age is %d \n",age);
    printf("pi is %f \n",pi);
    printf("star looks like this %c \n",star);
    
    // %d , %f , %c  is a format specifier. It specifies the output in which types. 
    
    
    return 0 ; 
}